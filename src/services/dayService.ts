import { faker } from "@faker-js/faker";

export const generateLondonDayData = (): DayData => {
  // Generate random day data
  const generatedDayData = {
    dayOne: faker.number.int({ min: -15, max: 30 }),
    dayTwo: faker.number.int({ min: 79, max: 86 }),
    dayThree: faker.number.int({ min: 2, max: 78 }),
  };

  // Return Day data
  return generatedDayData;
};

